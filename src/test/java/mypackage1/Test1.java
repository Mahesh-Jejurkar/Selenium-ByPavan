package mypackage1;

public class Test1 {
	/*
	 * //private access modifier private int x=100; private void m1() {
	 * System.out.println("This is m1() from Test1..."); }
	 */
	
	/*
	 * //default access modifier - if not specified. int x=100; void m1() {
	 * System.out.println("This is m1() from Test1..."); }
	 */
	
	/*
	 * //protected access modifier protected int x=100; protected void m1() {
	 * System.out.println("This is m1() from Test1..."); }
	 */
	
	//protected access modifier
	public int x=100;
	public void m1() {
		System.out.println("This is m1() from Test1...");
	}
	
}
